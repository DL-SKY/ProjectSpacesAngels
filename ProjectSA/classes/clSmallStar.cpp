void clSmallStar::Draw()
{
	//void Pr2D_Circle( float cX, float cY, float Radius, int Color, BYTE Alpha = 255, int Quality = 32, int Flags = PR_DEFAULT );
	//Рисуем светлую окружность
	Pr2D_Circle(fXpos * fZoom, fYpos * fZoom, (fWidth / 2 * 1.5) * fZoom, iColour, iAlpha/3, 32, PR_FILL);
	//Рисуем сердцевину
	Pr2D_Circle(fXpos * fZoom, fYpos * fZoom, (fWidth / 2) * fZoom, iColour, iAlpha, 32, PR_FILL);
	
	return;
}
//---------------------------------------------------------------------------
void clSmallStar::Update()
{
	if (bAlphaUp)
	{
		if (iAlpha + fSpeed > 255)
			bAlphaUp = false;
		else
			iAlpha += fSpeed;
	}
	else
	{		
		if (iAlpha - fSpeed < 0)
			bAlphaUp = true;
		else
			iAlpha -= fSpeed;
	}

	return;
}
